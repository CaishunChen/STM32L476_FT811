/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{
  /* init code for FATFS */
  MX_FATFS_Init();

  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 5 */


  I2C_Init();
  backlight_init();
  display_init(1);
  graphics_init();
  SAMAPP_GPU_Points();
  int screen=3;
  /* Infinite loop */
  for(;;)
  {
    switch (screen) {


		case 3:
			SAMAPP_GPU_Clear();
			printf("clear drawed\r\n");
		break;
		case 0:
			SAMAPP_GPU_Points();
			printf("points drawed\r\n");
			break;
		case 1:
			SAMAPP_GPU_Lines();
			printf("lines drawed\r\n");
			break;
		case 2:
			SAMAPP_GPU_Rectangles();
			printf("rectangles drawed\r\n");
			break;

		default:
			break;
	}

	HAL_GPIO_TogglePin(GPIOD,GPIO_PIN_4);
	osDelay(2000);
	screen++;
	if (screen>=4) screen=0;
  }
  /* USER CODE END 5 */ 
}

